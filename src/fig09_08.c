// Right Justifying integers in a field
#include <stdio.h>

int main(int argc, char const *argv[]) {
  printf("%4d\n", 1);
  printf("%4d\n", 12);
  printf("%4d\n", 123);
  printf("%4d\n", 1234);
  printf("%4d\n\n", 12345);
  printf("%4d\n", -1);
  printf("%4d\n", -12);
  printf("%4d\n", -123);
  printf("%4d\n", -1234);
  printf("%4d\n", -12345);
}
